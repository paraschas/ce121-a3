// file integers.c
////////////////////////////////////////////////////////////////////////////////
// Description
// A simple program that prints to stdout the integers from 1 to 128 in
// ascending order, one every 4 seconds or the number of seconds given as
// an argument. This value must be in the range [1, 32].
////////////////////////////////////////////////////////////////////////////////

// #include directives
////////////////////////////////////////////////////////////////////////////////
#include <stdio.h>
#include <unistd.h>
#include <stdlib.h>
#include <signal.h>
////////////////////////////////////////////////////////////////////////////////

// #define directives
////////////////////////////////////////////////////////////////////////////////
#define PROGRAM_DESCRIPTION "A simple program that prints to stdout the integers from 1 to 128 in ascending order, one every 4 seconds or the number of seconds given as an argument. This value must be in the range [1, 32]."
////////////////////////////////////////////////////////////////////////////////

// custom data types
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

// global variable declaration
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

// function prototypes
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

// functions
////////////////////////////////////////////////////////////////////////////////
static void signal_handler(int signal_received) {
    write(1, "signal received\n", 16);
}
////////////////////////////////////////////////////////////////////////////////

// tests
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

// main function
////////////////////////////////////////////////////////////////////////////////
int main(int argc, char *argv[]) {
    // Description
    // The main function implements the functionality described in the program
    // description.
    //
    // Returns
    // main returns 0 on successful completion or -1 in case of failure.

    // variable declaration
    struct sigaction action = { {0} };
    int delay;  // number of seconds to wait between successive outputs
    int return_value;  // integer placeholder for error checking
    int i;  // generic counter

    action.sa_handler = signal_handler;
    return_value = sigaction(SIGUSR1, &action, NULL);
    if (return_value == -1) {
        perror("error, sigaction");
        return -1;
    }

    printf("\n%s\n", PROGRAM_DESCRIPTION);

    if (argc < 2) {
        delay = 4;
    } else {
        return_value = atoi(argv[1]);
        if ((return_value <= 0) || (return_value > 32)) {
            // The number of seconds between successive outputs must be in
            // the range [1, 32].
            delay = 4;
        } else {
            delay = return_value;
        }
    }

    for (i = 0; i <= 128; i++) {
        printf("\n\tintegers: %d, delay: %d\n", i, delay);
        sleep(delay);
    }

    return 0;
}
